package rcc;

import org.junit.After;
import org.junit.Assert;
import org.junit.Before;
import org.junit.Test;

import java.util.Properties;

public class FakeForgingTest extends AbstractForgingTest {

    @Before
    public void init() {
        Properties properties = AbstractForgingTest.newTestProperties();
        properties.setProperty("rcc.disableGenerateBlocksThread", "false");
        properties.setProperty("rcc.enableFakeForging", "true");
        properties.setProperty("rcc.timeMultiplier", "1");
        AbstractForgingTest.init(properties);
        Assert.assertNotNull("rcc.fakeForgingPublicKeys must be defined in rcc.properties",
                             rcc.getStringProperty("rcc.fakeForgingPublicKeys"));
    }

    @Test
    public void fakeForgingTest() {
        forgeTo(startHeight + 10, testForgingSecretPhrase);
    }

    @After
    public void destroy() {
        AbstractForgingTest.shutdown();
    }

}
